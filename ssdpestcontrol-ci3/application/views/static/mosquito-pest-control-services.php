			$('.sliding_form').css('right', '-' + formWidth + 'px');
			$("#form_trigger").on('click', function() {

				if ($('.sliding_form').hasClass('slide_out')) {
					$('.sliding_form').removeClass('slide_out').addClass('slide_in')
					$(".sliding_form").animate({
						right: 0 + 'px'
